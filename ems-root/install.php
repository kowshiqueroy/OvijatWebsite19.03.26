<?php
// install.php — One-time setup wizard. Delete after installation.
session_start();
define('EMS_INSTALL', true);

$base           = __DIR__;
$installed_flag = $base . '/config/.installed';

if (file_exists($installed_flag)) {
    header('Location: index.php');
    exit;
}

$errors  = [];
$success = false;
$step    = isset($_POST['step']) ? (int)$_POST['step'] : 1;

// ── STEP 3: Process installation ─────────────────────────────────────────────
if ($_SERVER['REQUEST_METHOD'] === 'POST' && $step === 3) {
    $db_host   = trim($_POST['db_host']   ?? 'localhost');
    $db_name   = trim($_POST['db_name']   ?? '');
    $db_user   = trim($_POST['db_user']   ?? '');
    $db_pass   = $_POST['db_pass']        ?? '';
    $db_port   = trim($_POST['db_port']   ?? '3306');
    $sch_name  = trim($_POST['school_name']  ?? '');
    $sch_addr  = trim($_POST['school_address'] ?? '');
    $sch_phone = trim($_POST['school_phone']   ?? '');
    $sch_email = trim($_POST['school_email']   ?? '');
    $sch_type  = trim($_POST['school_type']    ?? 'school');
    $sch_board = trim($_POST['academic_board'] ?? 'DEB');
    $adm_user  = trim($_POST['admin_username'] ?? 'admin');
    $adm_pass  = $_POST['admin_password']      ?? '';
    $adm_name  = trim($_POST['admin_name']     ?? 'System Administrator');
    $adm_email = trim($_POST['admin_email']    ?? '');

    if (!$db_name)  $errors[] = 'Database name is required.';
    if (!$db_user)  $errors[] = 'Database username is required.';
    if (!$sch_name) $errors[] = 'School name is required.';
    if (strlen($adm_pass) < 6) $errors[] = 'Admin password must be at least 6 characters.';
    if ($adm_pass !== ($_POST['admin_password2'] ?? '')) $errors[] = 'Passwords do not match.';

    if (empty($errors)) {
        try {
            $dsn = "mysql:host={$db_host};port={$db_port};charset=utf8mb4";
            $pdo = new PDO($dsn, $db_user, $db_pass, [
                PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            ]);

            $pdo->exec("CREATE DATABASE IF NOT EXISTS `{$db_name}` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
            $pdo->exec("USE `{$db_name}`");

            foreach (explode('-- SPLIT --', get_schema_sql()) as $stmt) {
                $stmt = trim($stmt);
                if ($stmt) $pdo->exec($stmt);
            }

            seed_data($pdo, $sch_name, $sch_addr, $sch_phone, $sch_email, $sch_type, $sch_board,
                      $adm_user, $adm_pass, $adm_name, $adm_email);

            $cfg = "<?php\n// Auto-generated by EMS Installer. Do not edit manually.\n"
                 . "define('DB_HOST', '{$db_host}');\n"
                 . "define('DB_PORT', '{$db_port}');\n"
                 . "define('DB_NAME', '{$db_name}');\n"
                 . "define('DB_USER', '{$db_user}');\n"
                 . "define('DB_PASS', '{$db_pass}');\n";
            file_put_contents($base . '/config/db.php', $cfg);
            file_put_contents($installed_flag, date('Y-m-d H:i:s') . "\n");
            $success = true;
        } catch (PDOException $e) {
            $errors[] = 'Database error: ' . $e->getMessage();
        } catch (Exception $e) {
            $errors[] = 'Error: ' . $e->getMessage();
        }
    }
}

